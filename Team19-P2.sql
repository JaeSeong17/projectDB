CREATE TABLE ADMIN (
	ad_id	varchar(30)	NOT NULL,
	ad_pass	varchar(30)	NOT NULL,
	PRIMARY KEY (ad_id)
);

CREATE TABLE PRODUCERLOCATION (
	Pl_num INT(10) NOT NULL,
	Producer VARCHAR(30),
	Location VARCHAR(30),
	PRIMARY KEY (Pl_num)
);

CREATE TABLE STOCK_DATA (
	Office_num INT(10) NOT NULL,
	Product_number INT(10) NOT NULL,
	Stock INT(10),
	PRIMARY KEY (Office_num, Product_number)
);

CREATE TABLE CUSTOMER (
	Cus_id VARCHAR(30) NOT NULL,
	Cus_password VARCHAR(30),
	Address VARCHAR(30),
	Phone_num VARCHAR(30),
	First_name VARCHAR(30),
	Last_name VARCHAR(30),
	Sex CHAR,
	Birthdate DATE,
	Job VARCHAR(30),
	/*Count INT(11) AUTO_INCREMENT,*/
	PRIMARY KEY (Cus_id)
);

CREATE TABLE RETAILER(
	Office_num INT(10),
	Address VARCHAR(30),
	PRIMARY KEY (Office_num)
);

CREATE TABLE ITEM (
	Product_number	int(10) NOT NULL,
	Product_name 	varchar(30),
	Date_of_manufacture	DATE,
	Expiration_date	DATE,
	Importer	varchar(30),
	Price		int(10),
	PL_num	int(10),
	PRIMARY KEY (Product_number),
	FOREIGN KEY (PL_num) REFERENCES PRODUCERLOCATION(PL_num)
);

CREATE TABLE SHOPPINGBAG (
	Cus_id	VARCHAR(30),
	Product_number	int(10),
	Add_num	int(5),
	FOREIGN KEY(Cus_id) REFERENCES CUSTOMER(Cus_id),
	FOREIGN KEY(Product_number) REFERENCES ITEM(Product_number)
);

CREATE TABLE MAJOR_CATEGORY (
	Major_tag	VARCHAR(30)	NOT NULL,
	Major_number	int(10)	NOT NULL,
	PRIMARY KEY (Major_number)
);

CREATE TABLE MINOR_CATEGORY (
	Minor_tag	VARCHAR(30)	NOT NULL,
	Minor_number	int(10)	NOT NULL,
	Major_number	int(10) NOT NULL,
	PRIMARY KEY (Minor_number),
	FOREIGN KEY (Major_number) REFERENCES MAJOR_CATEGORY (Major_number)
);

CREATE TABLE CATEGORY (
	Product_number	int(10)	NOT NULL,
	Minor_number	int(10),
	Major_number	int(10),
	FOREIGN KEY(Product_number) REFERENCES ITEM(Product_number),
	FOREIGN KEY(Major_number) REFERENCES MAJOR_CATEGORY(Major_number),
	FOREIGN KEY(Minor_number) REFERENCES MINOR_CATEGORY(Minor_number)
	on delete cascade
	on update cascade
);
	
CREATE TABLE ORDERED (
	Order_num INT(10),
	Cus_id VARCHAR(30),
	Office_num INT(10),
	Ordered_date DATE,
	PRIMARY KEY (Order_num),
	FOREIGN KEY (Cus_id) REFERENCES CUSTOMER (Cus_id),
	FOREIGN KEY (Office_num) REFERENCES RETAILER (Office_num)
);

CREATE TABLE ORDERED_PRODUCT (
	Order_num INT(10),
	Product_number INT(10),
	Product_quantity INT(10),
	PRIMARY KEY (Order_num, Product_number),
	FOREIGN KEY (Order_num) REFERENCES ORDERED (Order_num),
	FOREIGN KEY (Product_number) REFERENCES ITEM (Product_number)
);

